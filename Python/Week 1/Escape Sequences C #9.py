#-------------------------------
# Escape Sequences Characters
# \b => Back Space
# \ newline => escape new line + \
# \   => Escape Back Slash
# \'  => Escape Single Quotes
# \"" => Escape Double Quotes
# \n  => Line Feed
# \r  => Carriage Return
# \t  => Hoizontal Tab
# \xhh=> Character Hex Value
#--------------------------------

# Back Space
print("Hello\b World")

# Escape New Line + \
print("Hello \
I Love \
Python") 

# Escape Back Slash
print("I Love Back Slash \\")

# Escape Doule Quotes 
print("I love Double Quoteds \"Test\"")

# Line Feed
print("Hello World\nSecond Line")

# Carriage Return
print("123456\rAbcde")

# Hoizontal Tab
print("Hello\tPython")

# Character Hex Value
print("\x4f\x73")

